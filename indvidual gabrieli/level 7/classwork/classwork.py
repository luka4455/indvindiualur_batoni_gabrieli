#1) გამოიყენე for loop სამჯერ და თტოეულ loop-ის გამოყენების დროს შეცვლაე რიცხვები
#2) დაპრინტე 1-დან 50-ამდე რიცხვები ყოველი 4-ე რიცხვის გამოტოვებით
#3) დაპრინტე "hello world" 100-ჯერ 
#4) დაპრიტნე 'hello" ცალკე "word" ცალკე ორივე 10-ჯერ იგივე for loop-ის გამოყენებით და for loop-ის დასრულების შემდეგ დაპრინტე "hello world".



#1)
for i in range(3):
    print(i)
for hello in range(10):
    print(hello)
for name in range(11):
    print(name)




